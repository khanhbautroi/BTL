    public void ht() throws SQLException{
        try {
            Connection kn = KN.KNDL();
            Statement stm = kn.createStatement();
            String sql = "select * from ql_sach";
            ResultSet rs = stm.executeQuery(sql);
            DefaultTableModel dtm = (DefaultTableModel) tb_tracuusach.getModel();
            dtm.setRowCount(0);
            
            while(rs.next()){
                Object object[]={
                    rs.getString("maS"),
                    rs.getString("tenS"),
                    rs.getString("tg"),
                    rs.getString("namXB"),
                    rs.getInt("gia"),   
                    rs.getString("sl"),
                    rs.getString("maNXB"),
                    rs.getString("ngonngu"),
                    rs.getString("tinhtrang")
                    
            };
                dtm.addRow(object);
                tb_tracuusach.setModel(dtm);
            }
        }catch (SQLException ex) {
            Logger.getLogger(TraCuuSach.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
    public void timkiem(){
        String tk = txt_tksach.getText();
            try{
                Connection kn = KN.KNDL();
                Statement stm = kn.createStatement();
                String sql = "select * from ql_nv where maNV like'"+tk+"' ";
                ResultSet rs = stm.executeQuery(sql);
                DefaultTableModel dtm = (DefaultTableModel) tb_tracuusach.getModel();
                dtm.setRowCount(0);
                while(rs.next()){
                    Object object[]={
                        rs.getString("maNV"),
                        rs.getString("tenNV"),
                        rs.getString("tuoi"),
                        rs.getString("gt"),
                        rs.getString("dc"),
                        rs.getString("sdt"),
                        rs.getString("email")
                    };
                    dtm.addRow(object);
                    tb_tracuusach.setModel(dtm);
                }
            } catch (SQLException ex) {
                Logger.getLogger(TraCuuSach.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    
