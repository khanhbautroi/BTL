    private void btn_truocActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_truocActionPerformed
        int n = tb_tracuusach.getSelectedRow();
        if(n>0){
            tb_tracuusach.setRowSelectionInterval(n-1, n-1);
        }else{
            tb_tracuusach.setRowSelectionInterval(tb_tracuusach.getRowCount()-1,tb_tracuusach.getRowCount()-1);
        }
        try {
            tbmouseClick();
        } catch (SQLException ex) {
            Logger.getLogger(TraCuuSach.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btn_truocActionPerformed

    private void btn_sauActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_sauActionPerformed
        int n = tb_tracuusach.getSelectedRow();
        if(n==tb_tracuusach.getRowCount()-1){
            tb_tracuusach.setRowSelectionInterval(0, 0);
        }else{
            tb_tracuusach.setRowSelectionInterval(n+1, n+1);
        }
        try {
            tbmouseClick();
        } catch (SQLException ex) {
            Logger.getLogger(TraCuuSach.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btn_sauActionPerformed

